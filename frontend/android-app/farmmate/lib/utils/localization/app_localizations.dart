  static String translate(String key, String languageCode) {
    // Default to Hindi if language not found
    String fallbackLanguage = languageCode == 'en' ? 'en' : 'hi';
    
    if (_localizedValues.containsKey(languageCode)) {
      return _localizedValues[languageCode]![key] ?? 
             _localizedValues[fallbackLanguage]![key] ?? 
             key;
    }
    
    return _localizedValues[fallbackLanguage]![key] ?? key;
  }

